Math.random()// random decimal number
Math.celi()//rounds up decimal number to highes, to the closest whole number
//Math.celi(2.000001) => 3
Math.floor//rounds up decimal number to lowest, to the closest whole number
//Math.floor(2.99999) => 2
Math.floor(Math.random() * 20) //random whole numbers, range from 0 - 19;
Math.celi(Math.random() * 20) //random whole number, range from 1 - 20;
